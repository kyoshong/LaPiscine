#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;
	long	a;

	a = nb;
	if (a == 0)
		write(1, "0", 1);
	else
	{
		if (a < 0)
		{
			write(1, "-", 1);
			a = a * (-1);
		}
		if (a < 10)
		{
			c = a + '0';
			write(1, &c, 1);
		}
		else
		{
			ft_putnbr(a / 10);
			ft_putnbr(a % 10);
		}
	}
}
