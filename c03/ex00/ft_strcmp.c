int		ft_strcmp(char *s1, char *s2)
{
	while (1)
	{
		if (*s1 != *s2)
			break ;
		if (*s1 == 0 || *s2 == 0)
			break ;
		s1++;
		s2++;
	}
	if (*s1 == *s2)
		return (0);
	else if (*s1 < *s2)
		return (-1);
	return (1);
}
